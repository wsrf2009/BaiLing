+ (NSString *)pinYin:(NSString *)chineseString {
    
    if ([chineseString isEqualToString:@""]) {
        return chineseString;
    }
    
    NSMutableString *source = [chineseString mutableCopy];
    CFStringTransform((__bridge CFMutableStringRef)source, NULL, kCFStringTransformMandarinLatin, NO); // 找出中文对应的拼音
    CFStringTransform((__bridge CFMutableStringRef)source, NULL, kCFStringTransformStripDiacritics, NO); // 去掉音标
    
    if ([[(NSString *)chineseString substringToIndex:1] compare:@"长"] == NSOrderedSame) {
        [source replaceCharactersInRange:NSMakeRange(0, 5)withString:@"chang"];
    }
    
    if ([[(NSString *)chineseString substringToIndex:1] compare:@"沈"] == NSOrderedSame) {
        [source replaceCharactersInRange:NSMakeRange(0, 4)withString:@"shen"];
    }
    
    if ([[(NSString *)chineseString substringToIndex:1] compare:@"厦"] ==NSOrderedSame) {
        [source replaceCharactersInRange:NSMakeRange(0, 3)withString:@"xia"];
    }
    
    if ([[(NSString *)chineseString substringToIndex:1] compare:@"地"] ==NSOrderedSame) {
        [source replaceCharactersInRange:NSMakeRange(0, 3)withString:@"di"];
    }
    
    if ([[(NSString *)chineseString substringToIndex:1] compare:@"重"] ==NSOrderedSame) {
        [source replaceCharactersInRange:NSMakeRange(0, 5) withString:@"chong"];
    }
    
    NSLog(@"%s chineseString:%@ source:%@", __func__, chineseString, source);
    
    return source;
}

